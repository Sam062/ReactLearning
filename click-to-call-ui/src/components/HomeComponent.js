import { useSpring, animated } from 'react-spring';

function Number({ data }) {
  const { number } = useSpring({
    from: { number: 0 },
    number: data,
    delay: 250, //After this delay, animation starts
    config: { mass: 1, tension: 500, friction: 50 }
  });
  return <animated.div className="display-1" >{number.to((data) => data.toFixed(0))}</animated.div>
}


